  function positionContext(){
    if(!S) return {position:0,ahead:null,behind:null,gapAhead:null,gapBehind:null};
    const order=S.cars.filter(x=>!x.out).sort((a,b)=>a.time-b.time);
    const car=me(),i=order.indexOf(car);
    return {position:i+1,ahead:i>0?order[i-1]:null,behind:i>=0&&i<order.length-1?order[i+1]:null,
      gapAhead:i>0?Math.max(0,car.time-order[i-1].time):null,
      gapBehind:i>=0&&i<order.length-1?Math.max(0,order[i+1].time-car.time):null};
  }
